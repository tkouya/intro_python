# complex_arithmetic.py: 複素数の四則演算
complex_c, complex_d = complex(3.0, 4.0), complex(-2, -5)

print('complex_c, complex_d = ', complex_c, complex_d)

# 同じデータ型どうしの四則演算
print('c + d = ', complex_c + complex_d)
print('c - d = ', complex_c - complex_d)
print('c * d = ', complex_c * complex_d)
print('c / d = ', complex_c / complex_d)

