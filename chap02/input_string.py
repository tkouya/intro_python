# input_string.py: 文字列の入力
str_a = input('str_a =? ') # str_aの入力
str_b = input('str_b =? ') # str_bの入力

# str_a, str_bの確認
print('str_a, str_b = ', str_a, str_b)

# 文字列の長さ
print('len(str_a), len(str_b) = ', len(str_a), len(str_b))

# 文字列の連結
print('str_a + str_b = ', str_a + str_b)

# 文字列の最初の一文字目と最後の文字を表示
print('First and last char of str_a = ', str_a[0], str_a[len(str_a) - 1])
print('First and last char of str_b = ', str_b[0], str_b[len(str_b) - 1])


