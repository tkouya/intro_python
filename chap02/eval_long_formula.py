# eval_long_formula.py: 複雑な式の値の計算

a = 20240403
b = 20250401
c = 20340331

# (a + b) / (c + b)
print('(a + b) / (c + b) = ', (a + b) / (c + b))

# a * b^5 * c
print('a * (b ** 5) * c = ', a * (b ** 5) * c)

# (a - b)^2 + (b - c)^3 + (c - a)^4
print('(a - b) ** 2 + (b - c) ** 3 + (c - a) ** 4 = ', (a - b) ** 2 + (b - c) ** 3 + (c - a) ** 4)

