# zeros_ones.py: 特殊なベクトルと行列
import numpy as np

# ゼロベクトルとゼロ行列
print('np.zeros(3) = ', np.zeros(3))
print('np.zeros((3, 3)) = \n', np.zeros((3, 3)))

# すべての要素が1のベクトルと行列
print('np.ones(3) = ', np.ones(3))
print('np.ones((3, 3)) = \n', np.ones((3, 3)))

